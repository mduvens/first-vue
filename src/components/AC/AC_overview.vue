    <h2 id="overview">Overview</h2>
    <p><strong>L9010 - Arquitectura de Computadores</strong>
      reviews fundamental structures in modern microprocessor and computer system architecture design.</p>
    <p>Tentative topics will include computer organization, instruction set design, memory system design, pipelining,
      and other techniques to exploit parallelism.</p>
    <p>We will also cover system level topics such as storage subsystems and basics of multiprocessor systems. The
      course will focus on quantitative evaluation of design alternatives while considering design metrics such as
      performance and power dissipation.</p>
